Com a evolução da sociedade, a segurança passou a tender para a prevenção, visando antecipar-se ao crime.<br/><br/>
A segurança patrimonial tem como objetivo prevenir e reduzir perdas patrimoniais de uma determinada organização ou de pessoas. <br/><br/>
Um conjunto de atividades é definido em um plano de segurança envolvendo: pessoas, tecnologia e inteligência. <br/><br/>
Assim, através do <strong>Decreto- Lei nº 1.034, de 09 de novembro de 1969 e nº 1.103, de 03 de março de 1970</strong>, surgiram as primeiras empresas de segurança e vigilância armada privada no Brasil.<br/><br/>
As atividades de segurança privada são reguladas, autorizadas e fiscalizadas pelo Departamento de Polícia Federal – DPF e são complementares às atividades de segurança pública nos termos da legislação específica.<br/><br/>
O serviço de segurança patrimonial pode ser armado ou desarmado, através de vigilantes treinados, uniformizados e munidos de sistemas de comunicação integrados.<br/><br/>
